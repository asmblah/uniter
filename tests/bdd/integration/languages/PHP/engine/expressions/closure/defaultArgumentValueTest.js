'use strict';

var _ = require('microdash'),
    engineTools = require('../../tools'),
    nowdoc = require('nowdoc'),
    phpTools = require('../../../tools');

describe('PHP Engine closure expression default argument value integration', function () {
    var engine;

    function check(scenario) {
        beforeEach(function () {
            engine = phpTools.createEngine(scenario.options);
        });

        engineTools.check(function () {
            return {
                engine: engine
            };
        }, scenario);
    }

    _.each({
        'self-executed closure with default argument value': {
            code: nowdoc(function () {/*<<<EOS
<?php
    $result = 4;

    function ($number = 10) use (&$result) {
        $result = $number;
    }();

    return $result;

EOS
*/;}), // jshint ignore:line
            expectedResult: 10,
            expectedStderr: '',
            expectedStdout: ''
        }
    }, function (scenario, description) {
        describe(description, function () {
            check(scenario);
        });
    });
});
